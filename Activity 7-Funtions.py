##      Activity 07 - Funtions

def add(value1, value2):
    return value1 + value2
    
def sub(value1, value2):
    return value1 - value2

def mult(value1, value2):
    return value1 * value2
    
def div(value1, value2):
    return value1 / value2

print(f"1. Addition {add(400, 5)}\v")
print(f"2. Subtraction {sub(995895, 938590)}\v")
print(f"3. Multiplying {mult(1000, 5)}\v")
print(f"4. Dividing {div(16004950, 10)}\v")
